//THIS PROGRAM IS TO PRINT THE NUMBER DOWNWARDS AND REVERSE UPWARDS
#include<stdio.h>
int main()
{
int a,b,c;
printf(" PLEASE ENTER THREE NUMBERS :");
scanf("%d %d %d",&a,&b,&c);
printf("YOUR NUMBERS FORWARD :\n %d\n %d\n %d\n",a,b,c);
printf("YOUR NUMBER REVERSED :\n %d\n %d\n %d\n",c,b,a);
return(0);
}
